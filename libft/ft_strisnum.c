#include "libft.h"

int	ft_strisnum(char *str)
{
	int	x;

	x = 0;
	while (str[x])
	{
		if (str[0] == 45 || (str[x] >= 48 && str[x] <= 57))
			x++;
		else
			return (-1);
	}
	return (0);
}
